def get_systemeid(platform: str) -> int:
    """
    Get ScreenScraper system ID for an ES-DE platform identifier.
    
    Args:
        platform: ES-DE platform identifier (e.g., 'nes', 'snes')
        
    Returns:
        ScreenScraper system ID
        
    Raises:
        KeyError: If platform is not mapped
    """
    platform_lower = platform.lower()
    
    if platform_lower not in PLATFORM_SYSTEMEID_MAP:
        raise KeyError(
            f"Platform '{platform}' not mapped to ScreenScraper system ID. "
            f"Run tools/generate_system_map.py to update mapping."
        )
    
    return PLATFORM_SYSTEMEID_MAP[platform_lower]
